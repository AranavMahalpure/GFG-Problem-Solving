class Solution {
  public:
    // Function which returns the  root of the flattened linked list.
    Node* convert(vector<int>& v) {
    if (v.empty()) return nullptr;

    Node* head = new Node(v[0]);
    Node* temp = head;

    for (int i = 1; i < v.size(); i++) {
        temp->bottom = new Node(v[i]);
        temp = temp->bottom;
    }

    return head;
}

    Node *flatten(Node *root) {
        // Your code here
        struct Node * t1=root;
        
        vector<int> v;
        while(t1){
            struct Node * t2=t1;
            while(t2){
                v.push_back(t2->data);
                t2=t2->bottom;
            }
            t1=t1->next;
        }
        sort(v.begin(),v.end());
        return convert(v);
    }
};


