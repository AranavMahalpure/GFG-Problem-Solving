class Solution
{
public:
    vector<pair<int, int>> findPairsWithGivenSum(Node *head, int target)
    {
        // code here
         vector<pair<int, int>> v;
    Node *lower = head;
    // Find the last node (higher)
    Node *higher = head;
    while (higher->next) {
        higher = higher->next;
    }
    // Two-pointer technique
    while (lower != higher && lower->prev != higher) {
        int sum = lower->data + higher->data;
        if (sum == target) {
            v.push_back({lower->data, higher->data});
            lower = lower->next;
            higher = higher->prev;
        } else if (sum < target) {
            lower = lower->next;
        } else {
            higher = higher->prev;
        }
    }
    return v;
    }
};

